Không chỉ vậy, người sở hữu có quyền trao đổi giữa các tài sản với điều kiện tài sản phải được đăng ký hình thức Timeshare. Ví dụ như chuyển đổi từ resort này sang resort khác trên toàn thế giới với điều kiện resort có mặt trong danh sách Timeshare.
